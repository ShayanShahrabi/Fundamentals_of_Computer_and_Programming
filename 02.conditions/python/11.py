operand1, operat, operand2 = input().split()
operand1 = float(operand1)
operand2 = float(operand2)

if operat == '+':
    print(operand1 + operand2)
elif operat == '-':
    print(operand1 - operand2)
elif operat == '*':
    print(operand1 * operand2)
elif operat == '/':
        print(operand1 / operand2)
